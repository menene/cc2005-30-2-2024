"""Tambi칠n es posible hacer esto aplicando una funci칩n:"""

def winner(row):
  if row["Home Team Goals"] > row["Away Team Goals"]:
    return row["Home Team Name"]
  elif row["Home Team Goals"] < row["Away Team Goals"]:
    return row["Away Team Name"]
